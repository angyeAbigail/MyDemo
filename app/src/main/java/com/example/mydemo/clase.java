package com.example.mydemo;

public class clase {

    String idC,nombreC,edadC,gaseosaC;

    public clase(String idC, String nombreC, String edadC, String gaseosaC) {
        this.idC = idC;
        this.nombreC = nombreC;
        this.edadC = edadC;
        this.gaseosaC = gaseosaC;
    }

    public String getIdC() {
        return idC;
    }

    public String getNombreC() {
        return nombreC;
    }

    public String getEdadC() {
        return edadC;
    }

    public String getGaseosaC() {
        return gaseosaC;
    }
}
